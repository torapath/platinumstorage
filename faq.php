        <h2>FREQUENTLY ASKED QUESTIONS</h2>
        <p>We have compiled a list of questions that can better assist you in understanding the benefits of a storage condo.</p>
        <ol>
          <li>
            <h5>Why own instead of rent?</h5>
            <p>When compared to renting equivalent space, one advantage of owning is the increased rights. Rental properties are always under the control of a landlord, which means that monthly rent could be raised or the lease terminated with short notice to the tenant. Renters face the real prospect that their rents will escalate in the future because of rising construction costs: land, steel and concrete have risen over the years. By owning the space, individuals can avoid such surprises. And a mortgage should remain relatively stable over the life of a loan.</p>
<p>The storage condominium owner seeks to lock in occupancy costs and build real estate equity. Buying space insulates one from rising base rents since mortgage payments are fixed by the terms of a loan, not the landlord's rising development costs, and the owner builds equity with the principal portion of his mortgage payments. The owner has the opportunity for equity growth as the market value of the condominium appreciates - a phenomenon that no one can guarantee but few would deny occurs quite regularly.
</p>
          </li>
          <li>
            <h5>What is a condominium?</h5>
            <p>A "condominium" means real estate, portions of which are designated for separate ownership and the remainder of which is designated for common ownership solely by the owners of the separate portions.
</p>
          </li>
          <li>
            <h5>What is common ownership?</h5>
            <p>The land and all common facilities in the condominium such as the parking lot, restrooms, etc., and the walls and roofs are owned and operated by the owners through their elected representatives (directors).</p>
<p>This joint ownership and operation means that no individual owner has control over the management and decision-making process. Owners must cooperate with one another as a group (called the condominium association). All owners must abide by the rules and regulations required. The association is there to protect the interest of all Platinum Storage owners.
</p>
          </li>
          <li>
            <h5>Is a warranty provided?</h5>
            <p>Yes. For every purchase there is a limited one year warranty. Seller warrants certain components of your unit against defects in the original material and workmanship. It covers the following components: roofs, concrete, plumbing, mechanical and electric. For details see warranty in sales contract.
</p>
          </li>
          <li>
            <h5>What do I own outright?</h5>
            <p>You will own the area formed by the walls, floor and ceiling of your unit and everything inside including interior partitions, cabinets, appliances (air-conditioner) and fixtures. The structural parts of the building are owned in common like the land.
</p>
          </li>
          <li>
            <h5>What are the restrictions?</h5>
            <p>Covenants, Conditions &amp; Restrictions (CC&amp;Rs) are put in force to benefit the Platinum Storage community collectively. With CC&amp;Rs, property values generally will appreciate better than if they were not in force. Many owners like CC&amp;Rs because they provide a level of predictability and an expectation of behavior that can be enforced if necessary.</p>
<p>CC&amp;Rs may restrict the following: Excessive noise, fire hazards, storing hazardous materials, running a retail business from a unit, attaching heavy objects to the walls, living in a unit, etc. If you are wondering if your usage is going to be restricted because of the CC&amp;Rs, then please contact us to talk in more detail about your lifestyle and garage space needs.
</p>
          </li>
          <li>
            <h5>Can I customize the unit?</h5>
            <p>Yes, we highly suggest adding your own "flair" to your space; however there are certain quality control issues that are stated in the CC&amp;Rs. If you would like some recommendations, we have teamed with several companies to provide our buyers with the highest quality of flooring, mezzanines, and more. No exterior changes are permitted.
</p>
          </li>
          <li>
            <h5>Are there property taxes?</h5>
            <p>Yes, since you are deeded a real piece of property therefore there will be property taxes associated with owning the property.
</p>
          </li>
          <li>
            <h5>Are there dues?</h5>
            <p>Yes. The Property Owners Association (POA) dues takes care of everything external on the property, including: security, common water and electrical usage, lawn and landscaping, pest control, property insurance, maintaining the bathrooms and insuring and maintaining the structure of the Platinum Storage project. 
</p>
          </li>
          <li>
            <h5>How are the POA dues allocated?</h5>
            <p>They are calculated using the following three (3) factors: the square footage of your unit, the total square footage of all the units, and the annual budget. 
</p>
          </li>
          <li>
            <h5>What about insurance?</h5>
            <p>Fire and extended coverage insurance is paid by the condominium association. However, it will be your responsibility to obtain insurance for the contents and get personal liability for your unit.
</p>
          </li>
          <li>
            <h5>Is it okay to work on my RV or other vehicles in my unit?</h5>
            <p>Absolutely, these personal warehouses are ideal for working on your RV or boat and are big enough to give you room to make repairs or polish your classic car. Your unit can be the ideal big garage-getaway for hanging out and working on your toys. There is generous in-unit electrical outlets, fluorescent lighting and climate control for more comfort. As you would expect, there will be restrictions on working outside of the units. The drive aisles and common areas will be kept clear and maintained in a neat and presentable condition.
</p>
          </li>
          <li>
            <h5>Can I sell or rent my condominium?</h5>
            <p>You may always sell your condominium, but the Directors of the Association must be given notice when you are selling your unit. Units may be rented but the renters must abide by the CC&amp;Rs. The owner of the unit must notify the Association prior to renting the unit as well as when the renter will vacate the facility.
</p>
          </li>
          <li>
            <h5>What kind of security is on site and in the units?</h5>
            <p>Strategically placed closed circuit security cameras record the comings and goings on the property. Each unit has a monitored heat detector. Each owner, at his expense, may install cameras in their own unit that can be monitored from their computer at home. Above all, since you are in a community of like minded neighbors, the present owners keep an eye out for each other.
</p>
          </li>
          <li>
            <h5>How wise is the drive-aisle?</h5>
            <p>HUGE 92' drive-aisle. You will find it easy to maneuver your RV, boat or trucks. 
</p>
          </li>
          <li>
            <h5>Is there an RV dump?</h5>
            <p>Yes.</p>
          </li>
          <li>
            <h5>Is there water or plumbing available in the units?</h5>
            <p>Yes, there are also exterior hose bibs every few units that owners can use to wash their car, boat, ATVs, cars, etc. Please refer to the CC&amp;Rs for prohibited uses.
</p>
          </li>
          <li>
            <h5>Can I install a bathroom in my unit?</h5>
            <p>Yes, if plumbing is stubbed up in your unit. However, keep in mind that common-area restrooms will be available for all unit owners.
</p>
          </li>
          <li>
            <h5>Do I have 24/7 access?</h5>
            <p>Yes. Yes. Yes. This is one of the most exciting differences between Platinum Storage and all the rest!
</p>
          </li>
          <li>
            <h5>How is the electricity paid?</h5>
            <p>Each large unit is metered for individual use. Therefore, these unit owners will be charged only for the electricity which they use. However, the common area electricity is paid as part of the annual POA. Also if you own a 550sqft unit or smaller, these units will be on a common area meter and the electricity will be part of your monthly condo dues.
</p>
          </li>
        </ol>
